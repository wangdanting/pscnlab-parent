package com.pscnlab.member.services.dtos;

import java.io.Serializable;

public class MemberPageQueryDTO implements Serializable {
    private Integer uuidRole;
    private String gender;
    private String name;
    private String telephone;

    public Integer getUuidRole() {
        return uuidRole;
    }

    public void setUuidRole(Integer uuidRole) {
        this.uuidRole = uuidRole;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }
}
